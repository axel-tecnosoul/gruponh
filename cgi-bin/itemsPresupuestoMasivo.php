		// Verificar que el archivo subido sea un Excel
		if ($file_mime_type == 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet' || $file_mime_type == 'application/vnd.ms-excel') {
			
			$file = $_FILES['csv']['tmp_name'];
			
			// Leer el archivo Excel
			$spreadsheet = IOFactory::load($file);
			$worksheet = $spreadsheet->getActiveSheet();
			$rows = $worksheet->toArray();
			$i=0;
			// Insertar los datos en la base de datos
			foreach ($rows as $row) {
				$i++;
				$nombreItem = $row[0];
				$cantidad = (int) $row[1];
				$unidadMedida = $row[2];
				$precio = (float) $row[3];
				
				if (!empty($nombreItem)) {
					if ($nombreItem != "Items") {
						$sql = "SELECT id from unidades_medida where unidad_medida like '%".$unidadMedida."%'";
						$q = $pdo->prepare($sql);
						$q->execute();
						$data = $q->fetch(PDO::FETCH_ASSOC);
						$idUM = $data['id'];

						$sql = "INSERT INTO `presupuestos_detalle`(`id_presupuesto`, `detalle`, `cantidad`, `id_unidad_medida`, `costo`, `precio`) VALUES (?,?,?,?,?,?)";
						$q = $pdo->prepare($sql);
						$q->execute(array($id,$nombreItem,$cantidad,$idUM,0,$precio));
					}

					
				} else {
					continue;
				}
			}
			
		}
		$sql = "INSERT INTO logs(`fecha_hora`, `id_usuario`, `detalle_accion`,`modulo`,link) VALUES (now(),?,'Modificación masiva de presupuesto','Presupuestos','verPresupuesto.php?id=$id')";
		$q = $pdo->prepare($sql);
		$q->execute(array($_SESSION['user']['id']));
		Database::disconnect();
		header("Location: listarPresupuestos.php");	
		
		
	}
    
